# строка целиком
print('Привет! Меня зовут Кеша, мне 2 года')

name = 'Кеша'
age = 2

# вывести строку вместе с переменными в терминал
print(name, age)

# с разделителем /
print(name, age, sep='/')
print(name, age, sep=' / : ; h ')

# end
print(name)
print(age)

print('----------->')
# в кавычках ' ' указываем  разделитель, например, ; или :
print(name, end='\n')
print(age, end=';')
print('end', end=';')
