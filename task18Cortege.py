# последовательности

friend_name = 'Max'
friends = ['Max', 'Leo', 'Kate']
roles = ('admin', 'guest', 'user')

# Типы данных
print(type(friend_name))
print(type(friends))
print(type(roles))

# Индексация
print(friend_name[1])
print(friends[1])
print(roles[1])

# Срезы
print(friend_name[1:])
print(friends[1:])
print(roles[1:])

# Длина
print(len(friend_name))
print(len(friends))
print(len(roles))
